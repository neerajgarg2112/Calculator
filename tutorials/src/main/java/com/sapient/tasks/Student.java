package com.sapient.tasks;

public class Student {
	
	private int id;
	private String name;
	private String address;
	private float percentage;
	private int age;
	private int salary;
	
	public Student(int id,String name,String address,float percentage,int age, int salary) {
		this.id = id;
		this.name = name;
		this.address = address;
		this.percentage = percentage;
		this.age = age;
		this.salary = salary;
	}

}
